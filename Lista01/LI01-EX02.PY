"""
Exercício 2:
Faça um programa que receba a base e altura de um triângulo e imprima a área dele.
"""

base_tri = float(input("Digite o valor da base do triângulo: "))
h_tri = float(input("Digite o valor da altura do triângulo: "))

Area = (base_tri*h_tri)/2

print(f"A área do triângulo é {Area}")